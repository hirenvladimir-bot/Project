`timescale 1ns / 1ps

module Wave_Data_Mapper (
    input  wire        clk_vga,        // 25MHz VGA像素时钟
    input  wire        rst_n,          // 全局复位

    // ---------------- 显存 BRAM 读取接口 (Port B) ----------------
    output wire [9:0]  bram_read_addr, // 读地址 (0~1023)
    input  wire [23:0] bram_dout,      // 读出的数据 {CH2[11:0], CH1[11:0]}

    // ---------------- VGA 当前扫描坐标 (来自时序发生器) ----------------
    input  wire [9:0]  hcount,         // 当前扫描的屏幕 X 坐标
    input  wire [9:0]  vcount,         // 当前扫描的屏幕 Y 坐标
    input  wire        display_mode,   // 0=双波形, 1=李萨如模式

    // ---------------- 计算输出的屏幕目标坐标 ----------------
    // 双波形模式下计算出的目标 Y 坐标
    output reg  [9:0]  ch1_target_y,
    output reg  [9:0]  ch2_target_y,

    // 李萨如模式下计算出的目标 (X, Y) 坐标
    output reg  [9:0]  lissajous_target_x,
    output reg  [9:0]  lissajous_target_y
);

    // =========================================================================
    // 1. 数据解包与地址驱动逻辑
    // =========================================================================
    wire [11:0] ch1_raw = bram_dout[11:0];
    wire [11:0] ch2_raw = bram_dout[23:12];
    reg [9:0] lissajous_scan_addr;
    // 在普通双波形模式下，横坐标(时间)即为 BRAM 地址
    // 为了防止画面跳动，我们可以根据触发位置加入偏移，这里用最简单的直接映射
    // (假设屏幕宽度 640 足够显示 0~639 地址的数据)
    assign bram_read_addr = (display_mode == 1'b0) ? hcount : lissajous_scan_addr;
                            // 在李萨如模式下，地址需要独立于 hcount 快速循环自增
                            // 这里预留给李萨如的高速扫描计数器 (下文详细说明)
                            

    // =========================================================================
    // 2. 李萨如模式专属：独立的高速扫描地址计数器
    // =========================================================================
    
    always @(posedge clk_vga or negedge rst_n) begin
        if (!rst_n) begin
            lissajous_scan_addr <= 10'd0;
        end else if (display_mode == 1'b1) begin
            // 只要在李萨如模式下，就不停地飞速轮询 1024 个点
            lissajous_scan_addr <= lissajous_scan_addr + 1'b1;
        end
    end

    // =========================================================================
    // 3. 核心坐标映射计算 (Coordinate Mapping)
    // =========================================================================
    // 我们的 XADC 数据范围是 0 ~ 4095。
    // 我们需要把这个数据"压缩"并"平移"到屏幕的指定区域。
    // 为了节省除法器资源，我们采用移位操作：除以 16 (右移 4 位)，将 4095 压缩到 255。

    always @(posedge clk_vga or negedge rst_n) begin
        if (!rst_n) begin
            ch1_target_y       <= 10'd0;
            ch2_target_y       <= 10'd0;
            lissajous_target_x <= 10'd0;
            lissajous_target_y <= 10'd0;
        end else begin
            // -----------------------------------------------------------------
            // A. 双通道独立波形计算区 (屏幕上下分屏)
            // -----------------------------------------------------------------
            // CH1 画在屏幕上半部分：中心点为 Y = 120。
            // 当数据最大(255)时，波形在上方(Y坐标小)；数据最小时，波形在下方(Y坐标大)
            ch1_target_y <= 10'd240 - {2'b00, ch1_raw[11:4]}; 
            
            // CH2 画在屏幕下半部分：中心点为 Y = 360。
            ch2_target_y <= 10'd480 - {2'b00, ch2_raw[11:4]};

            // -----------------------------------------------------------------
            // B. 李萨如图形坐标计算区 (屏幕正中央)
            // -----------------------------------------------------------------
            // 李萨如模式下，CH1 代表 X 轴，CH2 代表 Y 轴。
            // 屏幕分辨率 640x480，中心点坐标为 (320, 240)。
            // 我们将 0~4095 移位 3 位(除以8)，得到 0~511 的动态范围。
            // 将中心点 2048 映射到屏幕中心：
            
            // 计算 X 轴偏差 (CH1): 减去中心点 2048
            // 计算 Y 轴偏差 (CH2): 减去中心点 2048 (注意 VGA Y 轴朝下，需反向)
            
            // 最终目标坐标 = 屏幕中心点 +/- 换算后的波形偏移量
            lissajous_target_x <= 10'd320 + (ch1_raw[11:3] - 9'd256);
            lissajous_target_y <= 10'd240 - (ch2_raw[11:3] - 9'd256);
        end
    end

endmodule